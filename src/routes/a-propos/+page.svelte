  <h2>À propos de ce site Web</h2>

  <p>
    Ce site Web a été créé dans le cadre d'un projet scolaire.<br />
    Il a pour but de permettre aux élèves de participer et de suivre un tournoi de football inter-écoles.
  </p>
  <p>
    Ce site Web a été créé par Paul Saillant et est entretenu par l'équipe du
    <a href="https://le-jdl-laroche.cf" target="_blank">Journal du Lycée La Rochefoucauld</a> et de la Coupe Jules Rimet.<br />
  </p>

  <h3>Pages du site</h3>
  <ul>
    <li>La page <a href="/tournois">Tournois</a> permet de suivre les tournois collège et lycée.</li>
    <li>La page <a href="/equipes">Équipes</a> présentes les différentes équipes des tournois</li>
    <li>La page <a href="/replays">Replays</a> permet de revoir les matchs passés et filmés.</li>
    <li>La page <a href="/agenda">Agenda</a> permet de voir les matchs et évènements à venir.</li>
  </ul>

